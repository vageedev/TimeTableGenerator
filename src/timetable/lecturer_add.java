    private void laKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_laKeyPressed
        // TODO add your handling code here:

        char c = evt.getKeyChar();
        String id = la.getText();

        int length = id.length();

        //check if this textField contain only numbers and backspace is allowed
        if (Character.isDigit(c) || Character.isISOControl(c)) {

            //check if the employee id contains six digits
            if (length < 6) {

                la.setEditable(true);

            } else {

                la.setEditable(false);

            }

        } else {

            la.setEditable(false);
            JOptionPane.showMessageDialog(this, "Enter Only Numbers!");

        }
    }//GEN-LAST:event_laKeyPressed

