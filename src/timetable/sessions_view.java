    private void view_viewSessionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_view_viewSessionActionPerformed
        // TODO add your handling code here:
        
        FunctionSession f = new FunctionSession();
        
        ResultSet rs = null;

       
        String lecName = "lecturer";
        String subNames = "subName";
        String subCodes = "subCode";
        String tag = "tag";
        String group = "groupNo";
        String subgroup = "subgroupNo";
        String count = "students";
        String duration = "duration";
        String session = "sessions";        
                
        rs = f.find(sessionId_viewSession.getSelectedItem().toString());
        
        try {

            //view the details
            if (rs.next()) {
               
                lecturers_viewSession.setText(rs.getString("lecturer"));
                subName_viewSession.setSelectedItem(rs.getString("subName"));
                subCode_viewSession.setSelectedItem(rs.getString("subCode"));
                tag_viewSession.setSelectedItem(rs.getString("tag"));
                group_viewSession.setText(rs.getString("groupNo"));
                sub_viewSession.setSelectedItem(rs.getString("subgroupNo"));
                students_viewSession.setText(rs.getString("students"));
                duration_viewSession.setText(rs.getString("duration"));
                session_viewSession.setText(rs.getString("sessions"));
                
            } else {
                JOptionPane.showMessageDialog(null, "Sorry, No Data Found!");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
    }//GEN-LAST:event_view_viewSessionActionPerformed

