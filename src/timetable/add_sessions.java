    private void create_addSessionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create_addSessionActionPerformed
        // TODO add your handling code here:
        
        String sessionId = id_addSession.getText();
        String lecturer = lecturers_addSession.getText();
        String subName = subName__addSession.getSelectedItem().toString();
        String subCode = subCode_addSession.getSelectedItem().toString();
        String tag = tag_addSession.getSelectedItem().toString();
        String group = group_addSession.getText();
        String subgroup = sub2_addSession.getSelectedItem().toString();
        int count = Integer.parseInt(students_addSession.getText());
        String sessions = session_addSession.getText();
        String duration = duration_addSession1.getText();
        

        try {

            //Check if there are any empty field before adding details to the database
            if (duration_addSession1.getText().isEmpty() || lecturers_addSession.getText().isEmpty() || subName__addSession.getSelectedItem().equals("Select Subject Name") || subCode_addSession.getSelectedItem().equals("Select Subject Code") || tag_addSession.getSelectedItem().equals("Select Tag") || group_addSession.getText().isEmpty() || students_addSession.getText().isEmpty() || session_addSession.getText().isEmpty()) {

                JOptionPane.showMessageDialog(this, "Invalid Input");

            }

            else {

                //add a new session to the database
                Statement s = con.createStatement();
                String id = tag_addSession.getSelectedItem().toString();
                             
                if (id.equals("practical")){
                    
                    s.execute("INSERT INTO sessions(sessionID,lecturer,tag,groupNo,subgroupNo,subName,subCode,students,duration,sessions) values('" + sessionId + "','" + lecturer + "','" + tag + "','" + group + "'," + subgroup + ",'" + subName + "','" + subCode + "'," + count + ",'" + duration +"','"+ sessions +"')");

                        JOptionPane.showMessageDialog(this, "Records Are Submitted!");
                    
                }
                
                else {
                    
                     s.execute("INSERT INTO sessions(sessionID,lecturer,tag,groupNo,subName,subCode,students,duration,sessions) values('" + sessionId + "','" + lecturer + "','" + tag + "','" + group + "','" + subName + "','" + subCode + "'," + count + ",'" + duration +"','"+ sessions +"')");

                        JOptionPane.showMessageDialog(this, "Records Are Submitted!");
                
                }

            }

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(this, ex);

        }    
        
    }//GEN-LAST:event_create_addSessionActionPerformed

