    private void students_addSessionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_students_addSessionKeyPressed
        // TODO add your handling code here:
        
        char c = evt.getKeyChar();
        String id = students_addSession.getText();

        //check if this textField contain only numbers and backspace is allowed
        if (Character.isDigit(c) || Character.isISOControl(c)) {   

               students_addSession.setEditable(true);

        } else {

            students_addSession.setEditable(false);
            JOptionPane.showMessageDialog(this, "Enter Only Numbers!");

        }
        
    }//GEN-LAST:event_students_addSessionKeyPressed


